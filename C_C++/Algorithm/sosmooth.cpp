    #include<bits/stdc++.h>
    using namespace std;

    int main (){
        int c,b;
        cin >>c>>b;
        int a[]={12,23,34,45,56,67,78,89,123,234,345,456,567,678,789,1234,2345,3456,4567,5678,6789,12345,23456,34567,45678,56789,123456,234567,345678,456789,1234567,2345678,3456789,12345678,23456789,123456789};
        int S=0;
        for (int i=0;i<36;i++){
            if (a[i]>=c && a[i]<=b) {
                S++;
            }
        }
        cout <<S<<endl;
        cout <<'[';
        for (int i=0;i<36;i++){
            if (a[i]>=c && a[i]<=b) {
                cout<<'['<<a[i]<<']';
                S--;
                if (S>0) cout <<", ";
            }
        }
        cout <<']';
        return 0;
    }